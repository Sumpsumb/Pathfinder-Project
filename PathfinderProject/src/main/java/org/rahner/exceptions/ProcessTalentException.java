package org.rahner.exceptions;

public class ProcessTalentException extends Exception {

	public ProcessTalentException() {
		super();
	}

	public ProcessTalentException(String message) {
		super(message);
	}

	public ProcessTalentException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProcessTalentException(Throwable cause) {
		super(cause);
	}

}
