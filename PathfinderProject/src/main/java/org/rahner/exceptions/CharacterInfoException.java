package org.rahner.exceptions;

public class CharacterInfoException extends Exception {

	public CharacterInfoException() {
		super();
	}

	public CharacterInfoException(String message) {
		super(message);
	}

	public CharacterInfoException(String message, Throwable cause) {
		super(message, cause);
	}

	public CharacterInfoException(Throwable cause) {
		super(cause);
	}
}
