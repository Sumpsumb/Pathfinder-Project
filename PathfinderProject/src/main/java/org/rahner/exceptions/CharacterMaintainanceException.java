package org.rahner.exceptions;

public class CharacterMaintainanceException extends Exception {

	public CharacterMaintainanceException() {
		super();
	}

	public CharacterMaintainanceException(String message) {
		super(message);
	}

	public CharacterMaintainanceException(String message, Throwable cause) {
		super(message, cause);
	}

	public CharacterMaintainanceException(Throwable cause) {
		super(cause);
	}
}
